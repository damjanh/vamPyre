    def render(self, screen, offset: pg.Vector2):
        min_y = int(offset[1] // TILE_SIZE)
        max_y = int((screen.get_size()[1] + offset[1]) // TILE_SIZE) + 1

        min_x = int(offset[0] // TILE_SIZE)
        max_x = int((screen.get_size()[0] + offset[0]) // TILE_SIZE) + 1
        for i in range(min_y, max_y):
            for j in range(min_x, max_x):
                if 0 > i or i > self.map_height_tiles - 1 or 0 > j or j > self.map_width_tiles - 1:
                    surface = self.sprite_out_bounds
                else:
                    key = self.tile_map[i][j]
                    surface = self.sprite_dict.get(key)
                pos = pg.Vector2(x=j * TILE_SIZE, y=i * TILE_SIZE)
                offset_pos = pos - offset
                screen.blit(surface, offset_pos)
